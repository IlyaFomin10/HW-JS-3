// Task1
function cube(number) {
  return Math.pow(number, 3);
}
console.log(cube(2) + cube(3));

// Task2

let number = Number(
  prompt("Введите заработную плату до вычетов налогов в рублях")
);

const wage = (salary) => {
  return salary * 0.87;
};

if (Number.isNaN(number)) {
  alert("Введите заработную плату верно в цифрах");
} else {
  console.log(
    `Заработная плата равна ${wage(number)} рублей после вычета налога`
  );
}

// Task3

let num1 = Number(prompt("Введите число"));
let num2 = Number(prompt("Введите число"));
let num3 = Number(prompt("Введите число"));

function maxNumber(number1, number2, number3) {
  return Math.max(number1, number2, number3);
}

console.log(
  "Максимальное значение среди этих трех чисел - " + maxNumber(num1, num2, num3)
);

