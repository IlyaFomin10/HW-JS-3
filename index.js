const sum = (num1, num2) => {
  return num1 + num2;
};
const difference = (num1, num2) => {
  return Math.abs(num1 - num2);
};
const multiplication = (num1, num2) => {
  return num1 * num2;
};
const division = (num1, num2) => {
  return num1 / num2;
};

console.log(sum(num4, num5));
